// =====================================================
// ФАЙЛ: main.cpp (заливается в ESP32)
// ПРОГРАММА: ESP32 Button Launcher v3.0
//
// ЧТО ДЕЛАЕТ ЭТА ПРОГРАММА:
// ESP32 читает три физические кнопки и отправляет события
// на компьютер через USB (Serial порт).
//
// ДВА ТИПА НАЖАТИЯ:
//   Короткое нажатие → запустить назначенную программу
//   Длинное нажатие (2+ сек) → открыть диалог выбора новой программы
//
// ЖЕЛЕЗО:
//   ESP32 Dev Module  — основная плата (процессор 240 МГц, Wi-Fi, Bluetooth)
//   SSD1306 128x64    — маленький OLED дисплей (подключён по I2C)
//   BTN_1 на D4       — первая кнопка
//   BTN_2 на D16      — вторая кнопка
//   BTN_3 на D17      — третья кнопка
//   LED   на D2       — встроенный светодиод ESP32
//
// ПРОТОКОЛ ОБЩЕНИЯ С WINDOWS (что кому отправляем):
//   Windows → ESP32:  "TEST\n"         Windows проверяет: "Ты там?"
//                     "CONNECTED\n"    Windows говорит: "Я подключилась"
//                     "DISCONNECTED\n" Windows говорит: "Я отключаюсь"
//
//   ESP32 → Windows:  "ESP32_READY"    ESP32 стартовала и готова
//                     "ESP32_OK"       Ответ на TEST: "Да, это я!"
//                     "CONNECTED_OK"   Ответ на CONNECTED: "Понял, записал"
//                     "BTN_D4:1"       Короткое нажатие кнопки D4
//                     "BTN_D4:HOLD"    Длинное нажатие кнопки D4
//                     "BTN_D16:1"      Короткое нажатие кнопки D16
//                     "BTN_D16:HOLD"   Длинное нажатие кнопки D16
//                     "BTN_D17:1"      Короткое нажатие кнопки D17
//                     "BTN_D17:HOLD"   Длинное нажатие кнопки D17
// =====================================================

// =====================================================
// ПОДКЛЮЧЕНИЕ БИБЛИОТЕК
// Библиотека = готовый набор функций от других программистов
// =====================================================

#include <Arduino.h>          // Главная библиотека Arduino.
                              // Без неё не работают: pinMode, digitalRead,
                              // digitalWrite, millis, delay, Serial и т.д.

#include <Wire.h>             // Библиотека протокола I2C.
                              // I2C — способ соединить несколько устройств
                              // всего двумя проводами (SDA и SCL).
                              // Наш дисплей работает именно по I2C.

#include <Adafruit_GFX.h>     // Графическая библиотека от Adafruit.
                              // Умеет рисовать: текст, линии, прямоугольники,
                              // окружности, пиксели — для любых дисплеев.

#include <Adafruit_SSD1306.h> // Библиотека конкретно для нашего дисплея SSD1306.
                              // Знает как общаться именно с этим чипом по I2C.

// =====================================================
// НАСТРОЙКИ — константы которые не меняются во время работы
//
// #define — это инструкция препроцессора (работает ДО компиляции).
// Препроцессор находит все упоминания SCREEN_WIDTH в коде
// и заменяет их на 128 — буквально "найти и заменить".
// Удобно: хочешь поменять — меняешь только здесь, а не по всему коду.
// =====================================================

#define SCREEN_WIDTH  128   // Ширина дисплея в пикселях (128 точек по горизонтали)
#define SCREEN_HEIGHT 64    // Высота дисплея в пикселях (64 точки по вертикали)
#define OLED_RESET    -1    // Пин сброса дисплея.
                            // -1 означает "не используется" — наш дисплей
                            // не имеет отдельного пина сброса.
#define OLED_ADDR     0x3C  // Адрес дисплея на шине I2C.
                            // 0x3C — шестнадцатеричное число (= 60 в десятичной).
                            // Как номер квартиры: по этому адресу ESP32
                            // находит дисплей среди других I2C устройств.

#define LED_PIN   2         // Встроенный синий светодиод ESP32 — пин D2.
                            // Используем для сигнализации об ошибках.

#define BTN_1     4         // Первая кнопка подключена к пину D4.
#define BTN_2     16        // Вторая кнопка — пин D16.
#define BTN_3     17        // Третья кнопка — пин D17.

#define DEBOUNCE_MS   5     // Время антидребезга в миллисекундах.
                            // ПРОБЛЕМА ДРЕБЕЗГА: когда нажимаешь кнопку,
                            // металлические контакты внутри физически
                            // "отскакивают" друг от друга несколько раз
                            // за первые 5 миллисекунд. Программа видит это
                            // как: нажата-отпущена-нажата-отпущена...
                            // хотя ты нажал один раз. Это и есть дребезг.
                            // РЕШЕНИЕ: ждём 5 мс после изменения сигнала
                            // и только тогда считаем нажатие настоящим.

#define LONG_PRESS_MS 2000  // Время длинного нажатия в миллисекундах.
                            // 2000 мс = 2 секунды.
                            // Если кнопка удерживается дольше 2 секунд —
                            // это длинное нажатие (HOLD), иначе — короткое.

// =====================================================
// СОЗДАНИЕ ОБЪЕКТА ДИСПЛЕЯ
//
// Объект — это переменная которая не просто хранит число,
// а умеет выполнять действия (методы).
// display.clearDisplay() — очистить экран
// display.println("Hello") — написать текст
// display.display() — показать нарисованное на экране
//
// Параметры конструктора:
//   SCREEN_WIDTH  — ширина нашего дисплея (128)
//   SCREEN_HEIGHT — высота нашего дисплея (64)
//   &Wire         — указатель на объект I2C шины (& = адрес переменной)
//   OLED_RESET    — пин сброса (-1 = не используется)
// =====================================================
Adafruit_SSD1306 display(SCREEN_WIDTH, SCREEN_HEIGHT, &Wire, OLED_RESET);

// =====================================================
// ПЕРЕМЕННЫЕ СОСТОЯНИЯ КНОПКИ 1 (D4)
//
// Зачем так много переменных на одну кнопку?
// Нам нужно отслеживать:
//   1. Сырой сигнал (может дребезжать)
//   2. Стабильный сигнал (после антидребезга)
//   3. Когда началось нажатие (для определения длинного)
//   4. Отправляли ли уже HOLD (чтобы не отправить дважды)
// =====================================================

int btn1Raw = HIGH;
// "Сырое" состояние пина — то что digitalRead возвращает прямо сейчас.
// Может меняться много раз при дребезге.
// HIGH = 1 = кнопка НЕ нажата (внутренняя подтяжка держит +3.3V)
// LOW  = 0 = кнопка НАЖАТА (пин соединён с GND через кнопку)

int btn1LastStable = HIGH;
// Последнее ПОДТВЕРЖДЁННОЕ стабильное состояние.
// Меняется только когда сигнал стабилен дольше DEBOUNCE_MS миллисекунд.
// Это и есть "настоящее" состояние кнопки.

unsigned long btn1DebounceStart = 0;
// Момент времени (в мс от старта ESP32) когда сырой сигнал начал меняться.
// unsigned long — целое число без знака, может хранить до 4 294 967 295.
// millis() возвращает такие большие числа (миллисекунды накапливаются).

bool btn1Pressed = false;
// Удобный флаг для дисплея: true = кнопка сейчас нажата, false = отпущена.
// Используем чтобы нарисовать "PRESSED" или "released" на экране.

unsigned long btn1PressStart = 0;
// Момент когда кнопка была нажата (подтверждённо, после антидребезга).
// Нужен для подсчёта как долго кнопка удерживается.
// now - btn1PressStart = сколько миллисекунд кнопка уже нажата.

bool btn1HoldSent = false;
// Флаг: отправили ли мы уже сообщение о длинном нажатии (HOLD)?
// Нужен чтобы не отправлять HOLD многократно пока кнопка удерживается.
// Сбрасывается в false когда кнопка отпускается.

// =====================================================
// ПЕРЕМЕННЫЕ КНОПКИ 2 (D16) — те же самые, но для второй кнопки
// Каждая кнопка полностью независима — у неё свои переменные
// =====================================================
int           btn2Raw          = HIGH;
int           btn2LastStable   = HIGH;
unsigned long btn2DebounceStart = 0;
bool          btn2Pressed       = false;
unsigned long btn2PressStart    = 0;
bool          btn2HoldSent      = false;

// =====================================================
// ПЕРЕМЕННЫЕ КНОПКИ 3 (D17)
// =====================================================
int           btn3Raw          = HIGH;
int           btn3LastStable   = HIGH;
unsigned long btn3DebounceStart = 0;
bool          btn3Pressed       = false;
unsigned long btn3PressStart    = 0;
bool          btn3HoldSent      = false;

// =====================================================
// ОБЩИЕ ПЕРЕМЕННЫЕ
// =====================================================

bool displayNeedsUpdate = false;
// Флаг "нужно перерисовать дисплей".
// Вместо вызова updateDisplay() прямо внутри обработки кнопок,
// мы ставим этот флаг в true.
// В конце loop() один раз проверяем флаг и рисуем если нужно.
// Зачем такая схема? Рисование по I2C — медленная операция.
// Если обе кнопки изменились одновременно — лучше перерисовать
// один раз с обновлёнными данными, чем два раза подряд.

bool displayConnected = false;
// Флаг "Windows программа подключена".
// true  = Windows подключилась, показываем "PC CONNECTED" на дисплее
// false = Windows не подключена, показываем "READY"
// Меняется при получении команд CONNECTED и DISCONNECTED.

// =====================================================
// ФУНКЦИЯ: обновление дисплея
//
// Полностью перерисовывает всё содержимое экрана.
// Не принимает аргументов (скобки пустые) и ничего не возвращает (void).
// Использует глобальные переменные: displayConnected, btn1Pressed и т.д.
//
// ВАЖНО: все команды display.print/println рисуют только в памяти ESP32
// (в специальном буфере). На физический экран данные попадают ТОЛЬКО
// после вызова display.display() в самом конце функции.
// =====================================================
void updateDisplay() {

    display.clearDisplay();
    // Заполняем буфер чёрным цветом — стираем всё что было.
    // Это не мерцает на экране, потому что мы рисуем в памяти, не на экране.

    display.setTextSize(1);
    // Размер шрифта 1 = самый маленький стандартный шрифт Arduino.
    // Каждый символ занимает 6x8 пикселей.
    // На нашем экране 128x64 влезает: 21 символ в строке, 8 строк.

    // === ЗАГОЛОВОК (верхняя строка, y=0) ===
    display.setCursor(0, 0);
    // setCursor(x, y) — устанавливает позицию курсора.
    // x=0 — левый край, y=0 — самый верх экрана.

    if (displayConnected) {
        // Windows подключена — показываем радостное сообщение
        display.println(" ESP32: PC CONNECTED");
        // println — печатает строку и переводит курсор на новую строку.
    } else {
        // Windows не подключена — показываем что ESP32 готова
        display.println("   ESP32 v2.1 READY");
    }

    // === ГОРИЗОНТАЛЬНАЯ ЛИНИЯ-РАЗДЕЛИТЕЛЬ ===
    display.drawLine(0, 10, 127, 10, SSD1306_WHITE);
    // drawLine(x1, y1, x2, y2, цвет) — рисует линию от точки до точки.
    // (0, 10) — левый конец линии, (127, 10) — правый конец.
    // y=10 — на 10 пикселей ниже верха (под первой строкой текста).
    // SSD1306_WHITE — белый цвет (на чёрном фоне OLED это светящийся белый).

    // === СОСТОЯНИЕ КНОПКИ D4 (y=16) ===
    display.setCursor(0, 16);
    // y=16 — немного ниже линии-разделителя (которая на y=10)
    display.print("D4 : ");
    // print (без ln) — печатает текст БЕЗ перевода на новую строку.
    display.println(btn1Pressed ? "PRESSED" : "released");
    // Тернарный оператор: условие ? значение_если_правда : значение_если_ложь
    // Если btn1Pressed == true → печатаем "PRESSED"
    // Если btn1Pressed == false → печатаем "released"

    // === СОСТОЯНИЕ КНОПКИ D16 (y=28) ===
    display.setCursor(0, 28);
    // y=28 — на 12 пикселей ниже D4 (размер строки шрифта 1 = примерно 8-10 пикселей)
    display.print("D16: ");
    display.println(btn2Pressed ? "PRESSED" : "released");

    // === СОСТОЯНИЕ КНОПКИ D17 (y=40) ===
    display.setCursor(0, 40);
    display.print("D17: ");
    display.println(btn3Pressed ? "PRESSED" : "released");

    // === СТАТУС ПОДКЛЮЧЕНИЯ (нижняя строка, y=56) ===
    display.setCursor(0, 56);
    // y=56 — близко к нижнему краю экрана (высота 64 пикселя)
    display.println(displayConnected ? "Windows: ONLINE" : "Windows: offline");

    // === ОТПРАВКА БУФЕРА НА ФИЗИЧЕСКИЙ ЭКРАН ===
    display.display();
    // ЭТО САМАЯ ВАЖНАЯ СТРОКА!
    // Все предыдущие команды рисовали в буфере в памяти ESP32.
    // display.display() отправляет весь буфер на экран по I2C.
    // Без этой строки на экране ничего не изменится!
}

// =====================================================
// ФУНКЦИЯ: обработка одной кнопки с антидребезгом и длинным нажатием
//
// Это "мозг" программы. Вся сложная логика кнопок здесь.
//
// ПОЧЕМУ ОТДЕЛЬНАЯ ФУНКЦИЯ А НЕ КОД В loop()?
// У нас три кнопки с одинаковой логикой. Без отдельной функции
// пришлось бы писать один и тот же код три раза.
// С функцией — пишем один раз, вызываем три раза с разными параметрами.
// Это принцип DRY: Don't Repeat Yourself (не повторяй себя).
//
// ЧТО ЗНАЧИТ int &raw (с амперсандом &)?
// & означает "передать по ссылке" — функция получает доступ к ОРИГИНАЛЬНОЙ
// переменной, а не к её копии.
// Без &: функция получает копию → изменения не видны снаружи
// С &:   функция работает с оригиналом → изменения видны снаружи
//
// Пример:
//   void тест(int a)  { a = 10; }  // без ссылки
//   void тест(int &a) { a = 10; }  // со ссылкой
//   int x = 5;
//   тест(x);
//   // без ссылки: x = 5 (не изменилось)
//   // со ссылкой: x = 10 (изменилось!)
//
// ПАРАМЕТРЫ ФУНКЦИИ:
//   pin           — номер пина кнопки (4, 16 или 17)
//   raw           — сырое состояние (ссылка на btn1Raw/btn2Raw/btn3Raw)
//   lastStable    — стабильное состояние (ссылка)
//   debounceStart — время начала дребезга (ссылка)
//   pressed       — нажата ли кнопка (ссылка)
//   pressStart    — когда началось нажатие (ссылка)
//   holdSent      — отправляли ли HOLD (ссылка)
//   shortPressMsg — строка для короткого нажатия ("BTN_D4:1")
//   holdMsg       — строка для длинного нажатия ("BTN_D4:HOLD")
//
// const char* — указатель на строку в стиле C (массив символов).
// const — говорит что функция не будет изменять эту строку.
// =====================================================
void processButton(
  int pin,
  int &raw,
  int &lastStable,
  unsigned long &debounceStart,
  bool &pressed,
  unsigned long &pressStart,
  bool &holdSent,
  const char* shortPressMsg,
  const char* holdMsg)
{
    // Читаем ТЕКУЩЕЕ состояние пина.
    // digitalRead возвращает HIGH (1) или LOW (0).
    // Кнопка подключена через INPUT_PULLUP, поэтому:
    //   HIGH = кнопка НЕ нажата (пин подтянут к +3.3V через внутренний резистор)
    //   LOW  = кнопка НАЖАТА (пин соединён с GND через контакты кнопки)
    int currentRaw = digitalRead(pin);

    // Сохраняем текущее время для всех расчётов в этом вызове функции.
    // millis() возвращает сколько миллисекунд прошло с момента старта ESP32.
    // Используем одно значение для всего вызова — это важно:
    // если бы мы вызывали millis() в разных местах, время могло бы
    // незначительно отличаться и привести к ошибкам в логике.
    unsigned long now = millis();

    // ========== ШАГ 1: АНТИДРЕБЕЗГ ==========

    // Если сырой сигнал изменился с прошлого раза...
    if (currentRaw != raw) {
        raw = currentRaw;       // ...запоминаем новое сырое значение...
        debounceStart = now;    // ...и фиксируем МОМЕНТ НАЧАЛА изменения.
        // С этого момента мы ждём DEBOUNCE_MS миллисекунд.
        // Если сигнал останется стабильным всё это время — значит
        // дребезг закончился и это настоящее нажатие/отпускание.
    }

    // Если прошло DEBOUNCE_MS мс с последнего изменения сигнала
    // И текущий сигнал отличается от последнего стабильного...
    if (now - debounceStart >= DEBOUNCE_MS && currentRaw != lastStable) {

        // ...значит сигнал стабилен — обновляем стабильное состояние
        lastStable = currentRaw;

        if (lastStable == LOW) {
            // ===== КНОПКА НАЖАТА =====

            pressed    = true;      // Обновляем флаг для дисплея
            pressStart = now;       // Запоминаем момент нажатия
            holdSent   = false;     // Сбрасываем флаг длинного нажатия

            displayNeedsUpdate = true;  // Просим перерисовать дисплей

            // ВАЖНО: сообщение НЕ отправляем сразу!
            // Ждём пока кнопка отпустится — тогда узнаем короткое это или длинное.
            // Длинное нажатие обрабатывается ниже (в шаге 2) пока кнопка удерживается.

        } else {
            // ===== КНОПКА ОТПУЩЕНА =====

            pressed = false;            // Обновляем флаг для дисплея
            displayNeedsUpdate = true;  // Просим перерисовать дисплей

            // Если мы НЕ отправляли HOLD пока кнопка была нажата —
            // значит это было КОРОТКОЕ нажатие, отправляем сейчас.
            if (!holdSent) {
                Serial.println(shortPressMsg);
                // Serial.println отправляет строку в USB порт и добавляет \n в конце.
                // Windows программа читает это через COM-порт.
                // shortPressMsg = "BTN_D4:1" или "BTN_D16:1" или "BTN_D17:1"
            }
            // Если holdSent == true — длинное нажатие уже было отправлено,
            // короткое не отправляем (кнопка использована как HOLD).
        }
    }

    // ========== ШАГ 2: ПРОВЕРКА ДЛИННОГО НАЖАТИЯ ==========
    // Этот блок выполняется КАЖДУЮ итерацию loop() пока кнопка нажата.
    // Проверяем три условия одновременно:
    //   pressed      — кнопка сейчас нажата?
    //   !holdSent    — мы ещё НЕ отправляли HOLD?
    //   now - pressStart >= LONG_PRESS_MS — прошло ли 2 секунды?
    if (pressed && !holdSent && (now - pressStart >= LONG_PRESS_MS)) {

        holdSent = true;            // Ставим флаг — HOLD отправлен
        Serial.println(holdMsg);    // Отправляем "BTN_D4:HOLD" (или D16/D17)
        displayNeedsUpdate = true;  // Можно обновить дисплей (например показать "HOLD")

        // holdSent = true теперь запретит отправку shortPressMsg
        // когда кнопка будет отпущена (см. шаг 1 выше).
    }
}

// =====================================================
// setup() — ВЫПОЛНЯЕТСЯ ОДИН РАЗ ПРИ ВКЛЮЧЕНИИ ESP32
//
// Это "фаза подготовки" — настраиваем всё железо.
// После setup() начинается бесконечный loop().
// Аналог: компьютер загружается (setup) и потом работает (loop).
// =====================================================
void setup() {

    // ===== НАСТРОЙКА ПИНОВ =====
    // Каждый пин ESP32 можно настроить на вход или выход.

    pinMode(LED_PIN, OUTPUT);
    // OUTPUT = "выход" — мы управляем этим пином.
    // Можем подавать HIGH (+3.3V) или LOW (0V).
    // Светодиод подключён к пину D2 — подаём HIGH чтобы он светился.

    pinMode(BTN_1, INPUT_PULLUP);
    // INPUT_PULLUP = "вход с внутренней подтяжкой к питанию".
    // Внутри ESP32 есть резистор который соединяет пин с +3.3V.
    // Когда кнопка НЕ нажата: пин через резистор подтянут к +3.3V → HIGH.
    // Когда кнопка нажата: пин напрямую соединён с GND → LOW.
    // Без INPUT_PULLUP пин "висел бы в воздухе" и давал случайные значения.

    pinMode(BTN_2, INPUT_PULLUP);  // То же самое для кнопки D16
    pinMode(BTN_3, INPUT_PULLUP);  // То же самое для кнопки D17

    digitalWrite(LED_PIN, LOW);
    // digitalWrite устанавливает уровень на пине OUTPUT.
    // LOW = 0V = светодиод выключен.
    // Выключаем при старте чтобы не светился просто так.

    // ===== НАСТРОЙКА SERIAL (связь с компьютером) =====
    Serial.begin(115200);
    // Serial = последовательный порт (USB).
    // begin(115200) = открываем порт со скоростью 115200 бод.
    // Бод = бит в секунду. 115200 бод = примерно 11 520 байт/сек.
    // Скорость ДОЛЖНА совпадать с настройками Windows программы.

    delay(1000);
    // Ждём 1 секунду = 1000 миллисекунд.
    // Зачем? USB соединение устанавливается не мгновенно.
    // Если отправить данные слишком рано — они могут потеряться.
    // delay() — блокирующая пауза (ESP32 ничего не делает всё это время).
    // Здесь это нормально — происходит один раз при старте.

    Serial.println("ESP32_READY");
    // Сообщаем Windows программе что ESP32 запустилась и готова к работе.
    // println = print + перевод строки (\n в конце).
    // Windows программа ищет "ESP32_READY" среди первых сообщений.

    // ===== ИНИЦИАЛИЗАЦИЯ ДИСПЛЕЯ =====
    if (!display.begin(SSD1306_SWITCHCAPVCC, OLED_ADDR)) {
        // display.begin() — запускает дисплей и возвращает true если успешно.
        // ! означает "НЕ": если возвращает false (дисплей не найден)...
        // SSD1306_SWITCHCAPVCC — тип питания дисплея:
        //   внутренний повышающий преобразователь напряжения.
        // OLED_ADDR = 0x3C — адрес дисплея на шине I2C.

        Serial.println("Display init failed!");
        // Дисплей не нашли — сообщаем об ошибке в Serial Monitor.

        // Входим в бесконечный цикл — программа застревает здесь.
        // Мигаем светодиодом каждые 200 мс чтобы визуально сигнализировать об ошибке.
        while (true) {
            digitalWrite(LED_PIN, !digitalRead(LED_PIN));
            // digitalRead(LED_PIN) — читаем текущее состояние пина.
            // ! — инвертируем: если был LOW (выкл) → HIGH (вкл), и наоборот.
            // Каждый вызов переключает светодиод.
            delay(200);  // Пауза 200 мс между переключениями
        }
    }

    display.clearDisplay();         // Очищаем возможный мусор в буфере памяти
    display.setTextColor(SSD1306_WHITE); // Белый текст (единственный цвет у OLED)
    display.setTextSize(1);         // Маленький шрифт
    updateDisplay();                // Рисуем начальный экран

    Serial.println("Ready.");       // Говорим в Serial что всё настроено
}

// =====================================================
// loop() — ВЫПОЛНЯЕТСЯ БЕСКОНЕЧНО ПОСЛЕ setup()
//
// После того как setup() завершился, ESP32 вызывает loop()
// снова и снова — тысячи раз в секунду.
// В каждой итерации мы обрабатываем все события.
//
// СТРУКТУРА КАЖДОЙ ИТЕРАЦИИ:
//   1. Читаем команды от Windows (если есть)
//   2. Обрабатываем кнопку 1 (D4)
//   3. Обрабатываем кнопку 2 (D16)
//   4. Обрабатываем кнопку 3 (D17)
//   5. Обновляем дисплей (если что-то изменилось)
// =====================================================
void loop() {

    // ========== 1. КОМАНДЫ ОТ WINDOWS ==========

    // Serial.available() — возвращает сколько байт уже пришло и ждёт в буфере.
    // Если > 0 — есть что читать.
    if (Serial.available() > 0) {

        // readStringUntil('\n') — читает всё до символа \n (конец строки).
        // Windows программа добавляет \n в конец каждой команды.
        // Возвращает строку без \n.
        String command = Serial.readStringUntil('\n');

        // trim() — убирает пробелы, \r, \n по краям строки.
        // \r — символ возврата каретки (Windows добавляет перед \n).
        // Без trim() сравнение "TEST" == "TEST\r" было бы false.
        command.trim();

        if (command == "TEST") {
            // Windows спрашивает "Ты там? Ты ESP32?"
            // Отвечаем — да, это я!
            // Windows программа ищет "ESP32_OK" в ответ на TEST.
            Serial.println("ESP32_OK");
        }
        else if (command == "CONNECTED") {
            // Windows сообщает что подключение установлено.
            // Обновляем дисплей — показываем "PC CONNECTED".
            displayConnected   = true;   // Запоминаем что Windows подключена
            displayNeedsUpdate = true;   // Просим обновить дисплей
            Serial.println("CONNECTED_OK");  // Подтверждаем получение
        }
        else if (command == "DISCONNECTED") {
            // Windows отключается — обновляем дисплей.
            displayConnected   = false;  // Windows отключилась
            displayNeedsUpdate = true;   // Просим обновить дисплей
            // Ответ не нужен — Windows уже закрывает порт.
        }
    }

    // ========== 2. КНОПКА 1 (D4) ==========
    // Вызываем processButton с ПЕРЕМЕННЫМИ КНОПКИ 1.
    // Передаём все переменные по ссылке (&) — функция изменит их напрямую.
    processButton(
        BTN_1,              // Пин D4 — откуда читать состояние
        btn1Raw,            // Сырое состояние (функция изменит эту переменную)
        btn1LastStable,     // Стабильное состояние (функция изменит)
        btn1DebounceStart,  // Время начала дребезга (функция изменит)
        btn1Pressed,        // Флаг нажата/отпущена (функция изменит)
        btn1PressStart,     // Время нажатия (функция изменит)
        btn1HoldSent,       // Флаг отправки HOLD (функция изменит)
        "BTN_D4:1",         // Сообщение при коротком нажатии
        "BTN_D4:HOLD"       // Сообщение при длинном нажатии
    );

    // ========== 3. КНОПКА 2 (D16) ==========
    // Точно такой же вызов но с ПЕРЕМЕННЫМИ КНОПКИ 2.
    // Кнопки полностью независимы — каждая со своим набором переменных.
    processButton(
        BTN_2,
        btn2Raw,
        btn2LastStable,
        btn2DebounceStart,
        btn2Pressed,
        btn2PressStart,
        btn2HoldSent,
        "BTN_D16:1",
        "BTN_D16:HOLD"
    );

    // ========== 4. КНОПКА 3 (D17) ==========
    processButton(
        BTN_3,
        btn3Raw,
        btn3LastStable,
        btn3DebounceStart,
        btn3Pressed,
        btn3PressStart,
        btn3HoldSent,
        "BTN_D17:1",
        "BTN_D17:HOLD"
    );

    // ========== 5. ОБНОВЛЕНИЕ ДИСПЛЕЯ ==========
    // Проверяем флаг — нужно ли перерисовать экран?
    if (displayNeedsUpdate) {
        updateDisplay();            // Перерисовываем весь экран
        displayNeedsUpdate = false; // Сбрасываем флаг — экран актуален
    }

    delay(1);
    // Пауза 1 мс в конце каждой итерации.
    // Зачем? Без паузы loop() выполнялся бы миллионы раз в секунду,
    // грея процессор без пользы. 1 мс — незаметно для пользователя,
    // но даёт процессору передохнуть.
    // При этом мы не теряем реакцию: 1 мс << 5 мс антидребезга << 2000 мс HOLD.
}
