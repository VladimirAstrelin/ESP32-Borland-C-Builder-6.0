    // "All files (*.*)|*.*" — показывать все файлы
    // | — разделитель между описанием и маской, и между разными типами.

    OpenDialog1->Title = "Select program to pin";
    // Заголовок диалогового окна — пользователь видит подсказку.

    OpenDialog1->InitialDir = "C:\\Windows\\System32\\";
    // Папка которая откроется по умолчанию.
    // System32 — там лежат calc.exe, mspaint.exe, notepad.exe.
