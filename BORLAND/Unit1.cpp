//
//   БЛОК 7: ОБРАБОТЧИКИ КНОПОК И СОБЫТИЙ ФОРМЫ
//   Реакция на действия пользователя в интерфейсе Windows
//
// ════════════════════════════════════════════════════════════════════

//---------------------------------------------------------------------------
// BTN_CONNECTClick — пользователь нажал кнопку CONNECT.
//
// ПОСЛЕДОВАТЕЛЬНОСТЬ ПОДКЛЮЧЕНИЯ (9 шагов):
//   1. Проверяем что ещё не подключены
//   2. Получаем выбранный COM-порт из списка
//   3. Формируем правильное имя порта для Windows API
//   4. Открываем порт через CreateFile
//   5. Читаем и настраиваем параметры порта (скорость 115200, 8N1)
//   6. Устанавливаем медленные таймауты (для проверки ESP32)
//   7. Проверяем что это наш ESP32 через CheckESP32
//   8. Устанавливаем быстрые таймауты (для рабочего режима)
//   9. Обновляем UI и запускаем таймер чтения
//---------------------------------------------------------------------------
void __fastcall TForm1::BTN_CONNECTClick(TObject *Sender)
{
    // ШАГ 1: Защита от повторного подключения
    if (connected)
    {
        ShowMessage("Already connected!");
        return;
    }

    // ШАГ 2: Получаем выбранный порт
    if (CMB_COM_PORT->ItemIndex < 0 ||
        CMB_COM_PORT->Items->Strings[CMB_COM_PORT->ItemIndex] == "No COM ports found")
    // ItemIndex < 0 — ничего не выбрано в списке
    // || — логическое ИЛИ
    // == "No COM ports found" — выбрана заглушка (портов нет)
    {
        ShowMessage("Please select a COM port.");
        return;
    }

    String portName = CMB_COM_PORT->Items->Strings[CMB_COM_PORT->ItemIndex];
    // Strings[ItemIndex] — строка выбранного элемента.
    // Например: "COM13"

    // ШАГ 3: Формируем правильное имя порта для Windows API
    String comPort;
    String numStr  = portName.SubString(4, portName.Length() - 3);
    // SubString(начало, длина) — вырезает подстроку.
    // Начало с позиции 4 (C++ Builder: с 1!), длина = Length()-3.
    // "COM13": Length()=5, SubString(4, 2) = "13"
    // "COM3":  Length()=4, SubString(4, 1) = "3"

    int portNum = StrToIntDef(numStr, 0);
    // StrToIntDef — конвертирует строку в целое число.
    // Второй аргумент (0) — значение если конвертация не удалась.
    // "13" → 13, "3" → 3

    if (portNum > 9)
        comPort = "\\\\.\\"+portName;
    // ВАЖНАЯ ОСОБЕННОСТЬ WINDOWS:
    // COM1...COM9   — открываются по простому имени: "COM3"
    // COM10 и выше — ТРЕБУЮТ специального префикса: "\\.\"
    //                Без него CreateFile вернёт ERROR_FILE_NOT_FOUND!
    // "\\\\.\\": \\\\ = \\, \\ = \, итого = "\\.\\"  
    // С именем порта: "\\\\.\\COM13" = "\\.\COM13" в реальной строке
    else
        comPort = portName;  // COM1-COM9: просто "COM3"

    // ШАГ 4: Открываем COM-порт
    hCom = CreateFile(
        comPort.c_str(),               // Имя устройства как C-строка
        GENERIC_READ | GENERIC_WRITE,  // Права доступа: читать И писать
        // GENERIC_READ | GENERIC_WRITE — побитовое ИЛИ комбинирует флаги
        0,                             // dwShareMode = 0 = эксклюзивный доступ
        // Другие программы (Serial Monitor) не смогут открыть этот порт пока мы его держим
        NULL,                          // lpSecurityAttributes = NULL = настройки безопасности по умолчанию
        OPEN_EXISTING,                 // dwCreationDisposition: открыть СУЩЕСТВУЮЩИЙ порт
        // (CREATE_NEW создал бы новый файл — для порта не имеет смысла)
        FILE_ATTRIBUTE_NORMAL,         // dwFlagsAndAttributes: обычные атрибуты
        NULL                           // hTemplateFile: шаблон — не используется для портов
    );
    // CreateFile — несмотря на название "создать файл", открывает любые устройства.
    // В Windows ВСЁ является файлом: диски, принтеры, порты.
    // Возвращает HANDLE — дескриптор (номерок) открытого устройства.
    // При ошибке возвращает INVALID_HANDLE_VALUE.

    if (hCom == INVALID_HANDLE_VALUE)
    {
        DWORD err = GetLastError();
        // GetLastError() — возвращает код последней ошибки Windows API.
        // Нужно вызывать СРАЗУ после неудавшейся операции.

        String msg = "Cannot open " + portName + ".\n";
        if      (err == ERROR_ACCESS_DENIED)   msg += "Port is busy. Close Serial Monitor.";
        else if (err == ERROR_FILE_NOT_FOUND)  msg += "Port not found.";
        else                                   msg += "Error code: " + IntToStr((int)err);
        // IntToStr — конвертирует число в строку. (int) — приведение типов.

        ShowMessage(msg);
        return;
    }

    // ШАГ 5: Настраиваем параметры порта
    DCB dcb = {0};
    // DCB = Device Control Block — структура Windows с настройками COM-порта.
    // = {0} — инициализируем ВСЕ поля нулями.
    // Важно: неинициализированная структура содержит случайный "мусор".

    dcb.DCBlength = sizeof(dcb);
    // Обязательное требование Windows: указать размер структуры в её поле DCBlength.
    // Windows использует это чтобы различать разные версии структуры DCB.
    // sizeof(dcb) — размер структуры в байтах.

    if (!GetCommState(hCom, &dcb))
    // GetCommState — читает ТЕКУЩИЕ настройки порта в структуру dcb.
    // & — передаём АДРЕС переменной dcb, а не её копию.
    // Зачем читать если мы всё равно перезапишем?
    // В DCB много полей которые мы не трогаем — они должны остаться с системными значениями.
    {
        CloseHandle(hCom);   // Закрываем порт (освобождаем ресурс Windows)
        hCom = INVALID_HANDLE_VALUE;  // Помечаем как закрытый
        ShowMessage("Error reading port settings.");
        return;
    }

    // Устанавливаем стандарт 115200/8N1 как в ESP32 (Serial.begin(115200)):
    dcb.BaudRate = CBR_115200;
    // BaudRate — скорость в бодах (бит/сек). CBR_115200 = константа Windows = 115200.
    // ДОЛЖНА совпадать с Serial.begin(115200) в коде ESP32!

    dcb.ByteSize = 8;
    // ByteSize — количество бит данных в одном байте. Обычно 8.

    dcb.StopBits = ONESTOPBIT;
    // StopBits — количество стоп-бит (маркер конца байта). ONESTOPBIT = 1 стоп-бит.

    dcb.Parity = NOPARITY;
    // Parity — проверка чётности (метод обнаружения ошибок). NOPARITY = без проверки.

    if (!SetCommState(hCom, &dcb))
    // SetCommState — применяет новые настройки к порту.
    {
        CloseHandle(hCom);
        hCom = INVALID_HANDLE_VALUE;
        ShowMessage("Error applying port settings.");
        return;
    }

    // ШАГ 6: Медленные таймауты для проверки ESP32
    COMMTIMEOUTS checkTO;
    // COMMTIMEOUTS — структура Windows с таймаутами операций чтения/записи.
    // Таймаут = максимальное время ожидания операции.

    checkTO.ReadIntervalTimeout         = 50;
    // Максимальное время между получением двух последовательных байт (мс).
    // Если между байтами прошло больше 50 мс — ReadFile возвращает то что успел прочитать.

    checkTO.ReadTotalTimeoutMultiplier  = 10;
    // Множитель для расчёта общего времени ожидания: +10 мс на каждый запрошенный байт.

    checkTO.ReadTotalTimeoutConstant    = 50;
    // Константа общего времени ожидания: +50 мс базово.
    // Итоговый таймаут = ReadTotalTimeoutConstant + ReadTotalTimeoutMultiplier * запрошенных_байт

    checkTO.WriteTotalTimeoutMultiplier = 10;  // Аналогично для записи
    checkTO.WriteTotalTimeoutConstant   = 50;

    if (!SetCommTimeouts(hCom, &checkTO))
    // Применяем таймауты.
    {
        CloseHandle(hCom);
        hCom = INVALID_HANDLE_VALUE;
        ShowMessage("Error setting timeouts.");
        return;
    }

    PurgeComm(hCom, PURGE_RXCLEAR | PURGE_TXCLEAR);
    // Очищаем оба буфера перед проверкой — убираем данные накопившиеся при подключении.

    // ШАГ 7: Проверяем что это ESP32
    SB_MAIN_STATUS_BAR->SimpleText = "Checking ESP32...";
    Application->ProcessMessages();
    // ProcessMessages — даём Windows обработать все накопившиеся события.
    // Без этого надпись "Checking ESP32..." не появится на экране —
    // UI "замёрз" пока мы в этой функции.

    if (!CheckESP32(hCom))
    {
        CloseHandle(hCom);
        hCom = INVALID_HANDLE_VALUE;
        ShowMessage("ESP32 not detected on " + portName + "!\n"
                    "Make sure:\n"
                    "1. Correct COM port selected\n"
                    "2. ESP32 is powered\n"
                    "3. Sketch is running");
        SB_MAIN_STATUS_BAR->SimpleText = "Connection failed";
        return;
    }

    // ШАГ 8: Быстрые таймауты для рабочего режима
    COMMTIMEOUTS workTO;
    workTO.ReadIntervalTimeout         = 0xFFFFFFFF;
    // 0xFFFFFFFF = MAXDWORD = максимальное значение DWORD (4 294 967 295).
    // Это СПЕЦИАЛЬНОЕ ЗНАЧЕНИЕ описанное в документации MSDN.

    workTO.ReadTotalTimeoutMultiplier  = 0;
    workTO.ReadTotalTimeoutConstant    = 0;
    // Комбинация ReadIntervalTimeout=MAXDWORD + оба Multiplier/Constant=0:
    // "если в буфере ЕСТЬ данные → вернуть немедленно"
    // "если буфер ПУСТ → тоже вернуть немедленно с bytesRead=0"
    //
    // БЕЗ ЭТОГО: таймер вызывает ReadFile → ReadFile ЖДЁТ данных → UI замирает!
    // С ЭТИМ:   таймер вызывает ReadFile → ReadFile мгновенно возвращается → UI работает!

    workTO.WriteTotalTimeoutMultiplier = 10;  // Таймауты записи оставляем разумными
    workTO.WriteTotalTimeoutConstant   = 50;

    SetCommTimeouts(hCom, &workTO);
    PurgeComm(hCom, PURGE_RXCLEAR | PURGE_TXCLEAR);
    // Ещё раз очищаем буферы — CheckESP32 мог оставить там данные.

    // ШАГ 9: Всё готово — обновляем UI и запускаем таймер
    SetConnectedState(true, portName);
    SB_MAIN_STATUS_BAR->SimpleText = "Connected to ESP32 on " + portName;
}

//---------------------------------------------------------------------------
// BTN_DISCONNECTClick — пользователь нажал кнопку DISCONNECT.
//
// ПОСЛЕДОВАТЕЛЬНОСТЬ ОТКЛЮЧЕНИЯ:
//   1. Останавливаем таймер (прекращаем читать порт)
//   2. Отправляем "DISCONNECTED" ESP32 (она обновит свой дисплей)
//   3. Закрываем дескриптор порта
//   4. Обновляем UI через SetConnectedState
//---------------------------------------------------------------------------
void __fastcall TForm1::BTN_DISCONNECTClick(TObject *Sender)
{
    if (!connected)
    {
        ShowMessage("Not connected!");
        return;
    }

    TimerReadCom->Enabled = false;
    // СНАЧАЛА останавливаем таймер.
    // Если остановить ПОСЛЕ CloseHandle — таймер успеет сработать ещё раз
    // и попытается читать из уже закрытого порта → ошибка.

    if (hCom != INVALID_HANDLE_VALUE)
    {
        char cmd[] = "DISCONNECTED\n";
        DWORD bytesWritten;
        WriteFile(hCom, cmd, strlen(cmd), &bytesWritten, NULL);
        // Отправляем "DISCONNECTED\n" НАПРЯМУЮ через WriteFile (не через SendCommand).
        //
        // Почему не SendCommand?
        //   1. SendCommand проверяет commandPending — возможно он равен true.
        //   2. При отключении нам не нужен ответ от ESP32.
        //   3. Нам нужно отправить И СРАЗУ закрыть порт.
        //
        // ESP32 получит команду и покажет на своём дисплее "Windows: offline".

        CloseHandle(hCom);
        // CloseHandle — закрывает дескриптор, освобождает ресурс Windows.
        // После этого порт можно открыть другой программой (например Serial Monitor).

        hCom = INVALID_HANDLE_VALUE;
        // Помечаем дескриптор как недействительный.
        // Теперь все проверки hCom == INVALID_HANDLE_VALUE будут правильно работать.
    }

    SetConnectedState(false, "");
    // Обновляем весь UI: кнопки, метку статуса, останавливаем таймер (уже остановлен).

    SB_MAIN_STATUS_BAR->SimpleText = "Disconnected";
}

//---------------------------------------------------------------------------
// BTN_PIN_TO_D4Click — пользователь нажал кнопку PIN TO D4.
//
// Открывает стандартный диалог Windows "Открыть файл".
// Пользователь выбирает .exe программу для кнопки D4.
// Путь сохраняется в переменную, на экран и в INI файл.
//---------------------------------------------------------------------------
void __fastcall TForm1::BTN_PIN_TO_D4Click(TObject *Sender)
{
    OpenDialog1->Title    = "Select program for D4 button";
    // Заголовок диалога — пользователь видит для какой кнопки выбирает.

    OpenDialog1->FileName = "";
    // Очищаем поле имени файла — диалог откроется без предвыбранного файла.

    if (OpenDialog1->Execute())
    // Execute() — показывает диалог.
    // Возвращает true если пользователь выбрал файл и нажал "Открыть".
    // Возвращает false если нажал "Отмена" или закрыл диалог.
    {
        D4_ProgramPath = OpenDialog1->FileName;
        // FileName — полный путь к выбранному файлу.
        // Например: "C:\Program Files\Notepad++\notepad++.exe"

        UpdateD4PathDisplay();
        // Обновляем EDIT_D4 и LBL_D4_PINNED_TO с новым путём.

        SaveSettings();
        // Немедленно сохраняем в INI файл.
        // Если программа упадёт — настройки не потеряются.

        SB_MAIN_STATUS_BAR->SimpleText = "D4 pinned to: " + ExtractFileName(D4_ProgramPath);
        // ExtractFileName — только имя файла, без пути. "notepad++.exe"
    }
    // Если пользователь нажал "Отмена" — блок if не выполняется, ничего не меняется.
}

