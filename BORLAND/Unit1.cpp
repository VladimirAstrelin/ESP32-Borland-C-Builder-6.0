//   БЛОК 5: ЗАПУСК ПРОГРАММ
//   Выполнение .exe файлов по командам от ESP32
//
// ════════════════════════════════════════════════════════════════════
//
// СПОСОБЫ ЗАПУСКА ПРОГРАММ В WINDOWS:
//
//   WinExec("calc", SW_SHOW)
//     Старый простой способ. Работает с именами из PATH (calc, notepad)
//     и с полными путями. Возвращает > 31 если успешно, <= 31 если ошибка.
//
//   ShellExecute(NULL, "open", "calc.exe", NULL, NULL, SW_SHOW)
//     Современный способ через Windows Shell. Умеет:
//     - открывать файлы по ассоциации (.pdf → Adobe Reader)
//     - запрашивать права администратора (UAC)
//     - открывать URL в браузере
//     Возвращает > 32 если успешно.
//
// МЫ ИСПОЛЬЗУЕМ ОБА:
//   WinExec — основной (быстрее, проще)
//   ShellExecute — запасной если WinExec не справился

//---------------------------------------------------------------------------
// ExecuteD4Program — запускает программу назначенную на кнопку D4.
//---------------------------------------------------------------------------
void TForm1::ExecuteD4Program()
{
    if (D4_ProgramPath.IsEmpty())
    // IsEmpty() — true если строка пустая (длина 0).
    {
        SB_MAIN_STATUS_BAR->SimpleText = "No program assigned to D4!";
        return;  // Программа не назначена — выходим
    }

    String cmd = "\"" + D4_ProgramPath + "\"";
    // Оборачиваем путь в кавычки.
    // Зачем? Если в пути есть пробелы — WinExec воспримет их как разделители.
    // БЕЗ кавычек: C:\Program Files\app.exe → WinExec запустит "C:\Program" с аргументом "Files\app.exe"
    // С кавычками: "C:\Program Files\app.exe" → WinExec правильно понимает весь путь
    // \" в C++ строке = символ кавычки (экранированный).

    UINT result = WinExec(cmd.c_str(), SW_SHOW);
    // cmd.c_str() — конвертируем String в C-строку (const char*).
    // SW_SHOW — константа Windows: показать окно нормально (не свёрнутым, не на весь экран).
    // Другие варианты: SW_HIDE (скрытое), SW_MAXIMIZE (максимальное), SW_MINIMIZE (свёрнутое).
    // Возвращает UINT (32-битное беззнаковое целое).
    // > 31 = успешно, <= 31 = ошибка (конкретный код ошибки).

    if (result <= 31)
    // WinExec не справился — пробуем ShellExecute как запасной вариант.
    {
        ShellExecute(
            NULL,                      // hwnd — дескриптор родительского окна (NULL = нет)
            "open",                    // lpOperation — операция: "open" = открыть/запустить
            D4_ProgramPath.c_str(),    // lpFile — путь к файлу (C-строка)
            NULL,                      // lpParameters — аргументы командной строки (нет)
            NULL,                      // lpDirectory — рабочая папка (NULL = текущая)
            SW_SHOW                    // nShowCmd — как показать окно
        );
        // ShellExecute не возвращает ошибку в нашем случае — просто пробуем.
    }

    SB_MAIN_STATUS_BAR->SimpleText = "Launched: " + ExtractFileName(D4_ProgramPath);
}

