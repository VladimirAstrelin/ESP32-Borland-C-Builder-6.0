    // Нужно вызвать после LoadSettings() чтобы показать загруженные пути.
    UpdateD4PathDisplay();
    UpdateD16PathDisplay();
    UpdateD17PathDisplay();

    SB_MAIN_STATUS_BAR->SimpleText = "Ready";
    // SimpleText — простой текст в строке статуса (без разделения на панели).
