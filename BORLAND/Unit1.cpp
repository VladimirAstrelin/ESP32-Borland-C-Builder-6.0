//   Сохранение и загрузка путей к программам между сессиями
//
// ════════════════════════════════════════════════════════════════════
//
// КАК ВЫГЛЯДИТ INI ФАЙЛ (esp32_pins.ini):
//
//   [Buttons]
//   D4=C:\Windows\System32\calc.exe
//   D16=C:\Windows\System32\mspaint.exe
//   D17=C:\Windows\System32\notepad.exe
//
// [Buttons] — секция (раздел). Помогает группировать настройки.
// D4=...    — ключ=значение. Ключ слева от =, значение справа.
//
// Файл лежит рядом с .exe программы.
// Пользователь может открыть его в блокноте и отредактировать вручную.

//---------------------------------------------------------------------------
// LoadSettings — читает пути из INI файла при старте программы.
//---------------------------------------------------------------------------
void TForm1::LoadSettings()
{
    String iniPath = ExtractFilePath(Application->ExeName) + "esp32_pins.ini";
    // ExtractFilePath — VCL функция, извлекает путь к папке из полного пути.
    // ExtractFilePath("C:\MyProgram\app.exe") → "C:\MyProgram\"
    // Application->ExeName — полный путь к нашему запущенному .exe файлу.
    // Итого: iniPath = папка_где_лежит_exe + "esp32_pins.ini"
    // Пример: "C:\MyProgram\esp32_pins.ini"

    if (FileExists(iniPath))
    // FileExists — проверяет существует ли файл по указанному пути.
    // Возвращает true если файл найден.
    {
        TIniFile *ini = new TIniFile(iniPath);
        // Создаём объект TIniFile который откроет и прочитает INI файл.

        D4_ProgramPath  = ini->ReadString("Buttons", "D4",  "calc.exe");
        // ReadString(секция, ключ, значение_по_умолчанию) — читает значение из INI.
        // "Buttons" — секция [Buttons]
        // "D4"      — ключ D4
        // "calc.exe" — что вернуть если ключ не найден в файле
        // Возвращает String с найденным значением.

        D16_ProgramPath = ini->ReadString("Buttons", "D16", "mspaint.exe");
        D17_ProgramPath = ini->ReadString("Buttons", "D17", "notepad.exe");

        delete ini;
        // ОБЯЗАТЕЛЬНО удаляем объект — освобождаем память и закрываем файл.

        SB_MAIN_STATUS_BAR->SimpleText = "Settings loaded from: " + iniPath;
    }
    else
    {
        // Файл не существует — первый запуск программы.
        // Остаются значения по умолчанию из конструктора TForm1.
        SB_MAIN_STATUS_BAR->SimpleText = "First run — using default program paths";
    }
}

//---------------------------------------------------------------------------
// SaveSettings — записывает пути в INI файл.
//
// Вызывается:
//   1. Когда пользователь выбирает программу через PIN TO D4/D16/D17
//   2. Когда пользователь выбирает через длинное нажатие кнопки (HOLD)
