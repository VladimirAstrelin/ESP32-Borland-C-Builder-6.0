    LBL_D4_PINNED_TO->Caption      = "D4 PINNED TO:";
    LBL_D16_PINNED_TO->Caption     = "D16 PINNED TO:";
    LBL_D17_PINNED_TO->Caption     = "D17 PINNED TO:";

    // Настройка таймера.
    // Enabled = false — таймер ВЫКЛЮЧЕН при старте.
    // Если оставить включённым — он начнёт читать порт который ещё не открыт.
