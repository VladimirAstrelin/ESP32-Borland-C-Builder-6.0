//---------------------------------------------------------------------------
// RefreshComPorts — читает реестр Windows и заполняет выпадающий список.
//
// ГДЕ WINDOWS ХРАНИТ СПИСОК COM-ПОРТОВ?
// В реестре по адресу: HKEY_LOCAL_MACHINE\HARDWARE\DEVICEMAP\SERIALCOMM
// Каждый подключённый COM-порт (физический или USB-адаптер) записан там:
//   \Device\Serial0 → COM1
//   \Device\VCP0    → COM13
// Мы читаем значения (правые части) и добавляем в список.
//---------------------------------------------------------------------------
void TForm1::RefreshComPorts()
{
    CMB_COM_PORT->Clear();
    // Clear() — удаляет все элементы из выпадающего списка.
    // Нужно перед повторным заполнением чтобы не было дублей.

    TRegistry *reg = new TRegistry();
    // new — выделяет память в "куче" (heap) и создаёт объект.
    // Возвращает указатель на созданный объект.
    // ПРАВИЛО ОБЯЗАТЕЛЬНОЕ: всё что создано через new — удалять через delete!
    // Иначе память "утечёт" — останется занятой навсегда (до закрытия программы).

    reg->RootKey = HKEY_LOCAL_MACHINE;
    // RootKey — свойство класса TRegistry.
    // Указываем с какой корневой ветки реестра начинать.
    // HKEY_LOCAL_MACHINE — ветка с настройками всего компьютера (не конкретного пользователя).
    // COM-порты — это оборудование компьютера, поэтому они здесь.
    // Другие корневые ветки:
    //   HKEY_CURRENT_USER  — настройки текущего пользователя
    //   HKEY_CLASSES_ROOT  — ассоциации файлов (.docx → Word)

    if (reg->OpenKeyReadOnly("HARDWARE\\DEVICEMAP\\SERIALCOMM"))
    // OpenKeyReadOnly — открывает ключ реестра только для чтения.
    // Возвращает true если ключ существует и открылся успешно.
    // Мы только читаем, поэтому ReadOnly (не нужны права на запись).
    // \\\\ в C++ строке = \\ в реальном пути = разделитель в реестре.
    // Реальный путь: HARDWARE\DEVICEMAP\SERIALCOMM
    {
        TStringList *values = new TStringList();
        // TStringList — динамический список строк из VCL.
        // Умеет: добавлять, удалять, сортировать, искать строки.
        // Используем его чтобы получить имена всех записей в ключе реестра.

        reg->GetValueNames(values);
        // GetValueNames — заполняет список именами ВСЕХ записей в текущем ключе.
        // Имена записей (не значения!) выглядят так: "\Device\Serial0", "\Device\VCP0".
        // Это как ключи в словаре — по ним потом читаем значения.

        for (int i = 0; i < values->Count; i++)
        // Цикл for перебирает все найденные имена.
        // i начинается с 0, увеличивается на 1 каждую итерацию, пока i < Count.
        // values->Count — количество элементов в списке.
        {
            String portName = reg->ReadString(values->Strings[i]);
            // values->Strings[i] — i-й элемент списка (индексация с 0!).
            //   Например: "\Device\VCP0"
            // reg->ReadString(имя) — читает ЗНАЧЕНИЕ записи по её имени.
            //   Например: ReadString("\Device\VCP0") вернёт "COM13"
            // portName теперь содержит имя порта: "COM1", "COM13" и т.д.

            if (portName.Pos("COM") == 1)
            // Pos("COM") — ищет подстроку "COM" в строке.
            // Возвращает позицию первого вхождения.
            // ВНИМАНИЕ: в C++ Builder строки индексируются с 1 (не с 0!).
            // == 1 означает "COM" стоит в самом начале строки.
            // Это фильтр: добавляем только строки начинающиеся с "COM".
            // Зачем? В реестре могут быть записи не являющиеся COM-портами.
            {
                CMB_COM_PORT->Items->Add(portName);
                // Items — коллекция элементов ComboBox.
                // Add(строка) — добавляет элемент в конец списка.
            }
        }

        delete values;    // Удаляем TStringList — освобождаем память
        reg->CloseKey();  // Закрываем ключ реестра (как закрыть папку)
    }

    delete reg;  // Удаляем TRegistry — освобождаем память

    if (CMB_COM_PORT->Items->Count == 0)
    // Count — количество элементов в списке ComboBox.
    // Если 0 — ни одного COM-порта не нашли.
    {
        CMB_COM_PORT->Items->Add("No COM ports found");
        // Добавляем заглушку — пользователь видит сообщение вместо пустого списка.
    }

    CMB_COM_PORT->ItemIndex = 0;
    // ItemIndex — индекс выбранного элемента.
    // 0 = выбираем первый элемент автоматически.
    // Без этого список был бы пустым (ничего не выбрано) даже если есть элементы.
}

//---------------------------------------------------------------------------
// CheckESP32 — убеждается что на указанном порту именно наш ESP32.
//
// КАК РАБОТАЕТ:
//   1. Очищаем буфер (убираем старые данные)
//   2. Отправляем команду "TEST\n"
//   3. Ждём 300 мс (ESP32 обрабатывает и отвечает)
//   4. Читаем ответ и ищем строку "ESP32_OK"
//   5. Повторяем до 3 раз если не получилось
//
// ЗАЧЕМ ТРИ ПОПЫТКИ?
// При подключении ESP32 через USB происходит ПЕРЕЗАГРУЗКА микроконтроллера
// (сигнал DTR сбрасывает чип). Первые 1-2 секунды ESP32 загружает скетч.
// Три попытки с паузами дают ей время "проснуться" и начать отвечать.
//
// Параметр:  hPort — дескриптор открытого COM-порта
// Возвращает: true = ESP32 найдена и отвечает, false = не найдена
//---------------------------------------------------------------------------
bool TForm1::CheckESP32(HANDLE hPort)
{
    if (hPort == INVALID_HANDLE_VALUE)
        return false;
    // Защитная проверка: если дескриптор недействителен — сразу выходим.
    // return false — возвращаем "не нашли" и выходим из функции.

    char testCmd[] = "TEST\n";
    // char[] — массив символов (строка в стиле языка C).
    // "TEST\n" — строка из 6 символов: T, E, S, T, \n (новая строка), \0 (конец строки).
    // \n обязателен — ESP32 читает данные до символа новой строки.

    DWORD bytesWritten;
    // DWORD = Double Word = 32-битное целое число без знака (от Windows API).
    // WriteFile запишет сюда сколько байт реально отправлено.
    // Мы это число не проверяем, но функция требует передать адрес куда записать.

    for (int attempt = 0; attempt < 3; attempt++)
    // Цикл: attempt = 0, 1, 2 (три попытки).
    // attempt++ — увеличиваем счётчик на 1 после каждой итерации.
    {
        PurgeComm(hPort, PURGE_RXCLEAR | PURGE_TXCLEAR);
        // PurgeComm — очищает буферы COM-порта.
        // PURGE_RXCLEAR — очистить буфер ПРИЁМА (данные пришедшие от ESP32).
        // PURGE_TXCLEAR — очистить буфер ПЕРЕДАЧИ (данные ожидающие отправки).
        // | — побитовое ИЛИ: комбинирует два флага в одну команду.
        // Зачем? В буфере могут лежать старые данные: "ESP32_READY", "Ready." и т.д.
        // Они могут помешать поиску "ESP32_OK".

        if (!WriteFile(hPort, testCmd, strlen(testCmd), &bytesWritten, NULL))
        // WriteFile — записывает данные в файл/устройство.
        //   hPort        — в какой порт пишем
        //   testCmd      — что пишем (указатель на массив символов)
        //   strlen(...)  — сколько байт писать (длина строки БЕЗ нулевого байта)
        //   &bytesWritten — куда записать результат (& = адрес переменной)
        //   NULL          — для синхронного режима всегда NULL
        // Возвращает true если успешно, false если ошибка.
        // ! — НЕ: "если НЕ успешно (ошибка)"...
        {
            Sleep(100);   // ...ждём 100 мс...
            // Sleep — функция Windows API, блокирует поток на заданное время.
            continue;     // ...и переходим к следующей итерации цикла.
            // continue — пропускает остаток тела цикла и идёт к следующей итерации.
        }

        Sleep(300);
        // Ждём 300 мс после отправки команды.
        // Зачем? ESP32 должна получить "TEST\n", обработать в loop(),
        // выполнить Serial.println("ESP32_OK") и данные должны дойти обратно.
        // Всё это занимает время. 300 мс — с запасом.
        // Sleep здесь ДОПУСТИМ — мы ещё не запустили таймер,
        // UI "заморожен" на время подключения — это нормально и ожидаемо.

        char buffer[256] = {0};
        // Буфер для ответа от ESP32.
        // char buffer[256] — массив из 256 символов.
        // = {0} — инициализируем ВСЕ элементы нулём.
        // Зачем? Иначе в массиве будет случайный "мусор" из памяти.
        // Нулевой байт важен: в C строки заканчиваются нулём ('\0').

        DWORD bytesRead;  // Сюда запишется сколько байт прочитали

        if (!ReadFile(hPort, buffer, sizeof(buffer)-1, &bytesRead, NULL))
        // ReadFile — читает данные из файла/устройства.
        //   hPort           — из какого порта читаем
        //   buffer          — куда записать прочитанные данные
        //   sizeof(buffer)-1 — максимум байт для чтения (255, оставляем место для \0)
        //   &bytesRead      — куда записать количество прочитанных байт
        //   NULL            — для синхронного режима
        {
            Sleep(100);
            continue;
        }

        if (bytesRead == 0)
        // Если прочитали 0 байт — ничего не пришло, следующая попытка.
        {
            Sleep(100);
            continue;
        }

        buffer[bytesRead] = '\0';
        // Ставим нулевой байт после последнего прочитанного символа.
        // В C/C++ строки должны заканчиваться '\0' (нулевой символ).
        // Без этого String(buffer) может прочитать лишние байты из памяти.
        // buffer[bytesRead] — элемент с индексом bytesRead (первый ПОСЛЕ данных).

        TStringList *lines = new TStringList();
        lines->Text = String(buffer);
        // lines->Text = строка — TStringList автоматически разбивает текст по \n.
        // String(buffer) — конвертируем C-строку в VCL String.
        // ESP32 могла прислать несколько строк подряд:
        //   "ESP32_READY\nReady.\nESP32_OK\n"
        // После разбивки: lines[0]="ESP32_READY", lines[1]="Ready.", lines[2]="ESP32_OK"

        bool found = false;
        for (int i = 0; i < lines->Count; i++)
        // Перебираем все строки в поиске "ESP32_OK".
        {
            if (lines->Strings[i].Trim() == "ESP32_OK")
            // Strings[i] — i-я строка в списке.
            // Trim() — убирает пробелы, \r, \n по краям.
            // \r — символ возврата каретки (Windows добавляет его перед \n).
            {
                found = true;   // Нашли "ESP32_OK"!
                break;          // Выходим из цикла — искать дальше не нужно.
            }
        }

        delete lines;  // Освобождаем память TStringList

        if (found)
            return true;  // ESP32 подтверждена! Выходим из функции с успехом.

        Sleep(100);  // Пауза перед следующей попыткой
    }

    return false;  // Три попытки исчерпаны — ESP32 не найдена
}

//---------------------------------------------------------------------------
// TimerReadComTimer — срабатывает каждые 50 мс пока подключены.
//
// ЭТО ГЛАВНЫЙ РАБОЧИЙ ЦИКЛ ПРОГРАММЫ.
// Аналог loop() в Arduino — выполняется непрерывно пока программа работает.
//
// Задачи за каждые 50 мс:
//   1. Читаем всё что накопилось в буфере COM-порта
//   2. Разбиваем на строки (может прийти несколько за раз)
//   3. Каждую строку передаём в ParseData для обработки
//   4. Проверяем таймаут: если 5 секунд нет ответа — сбрасываем ожидание
//---------------------------------------------------------------------------
void __fastcall TForm1::TimerReadComTimer(TObject *Sender)
{
    if (!connected || hCom == INVALID_HANDLE_VALUE)
        return;
    // Двойная проверка: если не подключены ИЛИ порт не открыт — выходим.
    // ! — НЕ: !connected = "не подключены".
    // || — логическое ИЛИ: выходим если хотя бы одно условие истинно.

    char  buffer[512];  // Буфер 512 байт — с запасом для нескольких сообщений
    DWORD bytesRead;

    if (ReadFile(hCom, buffer, sizeof(buffer)-1, &bytesRead, NULL))
    // ReadFile с БЫСТРЫМИ ТАЙМАУТАМИ (установленными в BTN_CONNECTClick).
    // Быстрые таймауты: ReadIntervalTimeout=0xFFFFFFFF, оба Multiplier=0, Constant=0.
    // Это специальная комбинация описанная в MSDN:
    //   "если в буфере есть данные → вернуть немедленно"
    //   "если буфер пуст → тоже вернуть немедленно с bytesRead=0"
    // Без этого ReadFile бы ЖДАЛ данных и заморозил бы интерфейс на это время!
    {
        if (bytesRead > 0)
        // Если что-то прочитали (0 = буфер был пуст — ничего не делаем)
        {
            buffer[bytesRead] = '\0';  // Завершаем строку нулём

            TStringList *lines = new TStringList();
            lines->Text = String(buffer);
            // За 50 мс могло накопиться несколько сообщений:
            // "BTN_D4:1\nBTN_D4:HOLD\n"
            // TStringList разобьёт на: ["BTN_D4:1", "BTN_D4:HOLD"]

            for (int i = 0; i < lines->Count; i++)
            {
                AnsiString line = AnsiString(lines->Strings[i]).Trim();
                // AnsiString — 8-битная строка Borland.
                // Конвертируем Unicode String → AnsiString для ParseData.
                // Trim() убирает пробелы и \r\n по краям.

                if (!line.IsEmpty())
                    ParseData(line);
                // IsEmpty() — возвращает true если строка пустая.
                // !IsEmpty() = не пустая → обрабатываем.
                // Пустые строки пропускаем — они появляются при разбивке.
            }

            delete lines;  // Освобождаем память
        }
    }
    else
    {
        DWORD err = GetLastError();
        // GetLastError() — возвращает код последней ошибки Windows.
        // Нужно вызывать СРАЗУ после неудавшейся операции —
        // следующий вызов любой функции может перезаписать этот код.

        if (err == ERROR_BROKEN_PIPE || err == ERROR_INVALID_HANDLE)
        // ERROR_BROKEN_PIPE    — соединение разорвано (кабель выдернули)
        // ERROR_INVALID_HANDLE — дескриптор недействителен
        // || — логическое ИЛИ: реагируем на любую из этих ошибок
        {
            BTN_DISCONNECTClick(NULL);
            // Вызываем обработчик DISCONNECT как будто нажали кнопку.
            // NULL — передаём NULL как Sender (кнопка не нажималась).
            ShowMessage("ESP32 disconnected unexpectedly!\nCheck USB cable.");
            // ShowMessage — показывает простое диалоговое окно с текстом.
            // \n — перевод строки внутри сообщения.
        }
    }

    if (commandPending && (GetTickCount() - commandStartTime > 5000))
    // Проверяем таймаут: если командаPending=true (ждём ответ)
    // И прошло больше 5000 мс (5 секунд) с момента отправки команды...
    // GetTickCount() - commandStartTime = сколько мс прошло с отправки
    {
        commandPending = false;
        // ...сбрасываем ожидание — ответ уже не придёт.
        SB_MAIN_STATUS_BAR->SimpleText = "Command timeout — no response from ESP32";
    }
}


