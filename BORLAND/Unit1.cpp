    // Пользователь видит путь но не может его редактировать напрямую.
    // Изменить путь можно только через кнопку PIN TO D4/D16/D17.

    EDIT_D4->Color  = clBtnFace;
    EDIT_D16->Color = clBtnFace;
    EDIT_D17->Color = clBtnFace;
    // clBtnFace — системный цвет поверхности кнопки (обычно серый).
    // Визуально показывает что поле неактивно для редактирования.
    // Обычные активные поля — белые, серые — только для чтения.

    // Заполняем список COM-портов из реестра Windows.
