//---------------------------------------------------------------------------
// SendCommand — отправляет текстовую команду в COM-порт.
//
// Перед отправкой проверяет два условия:
//   1. Мы подключены к ESP32?
//   2. Не ждём ли ответа на предыдущую команду?
//
// Параметр: command — текст команды БЕЗ \n (функция добавит \n сама)
//---------------------------------------------------------------------------
void TForm1::SendCommand(AnsiString command)
{
    if (!connected || hCom == INVALID_HANDLE_VALUE)
        return;
    // Если не подключены — молча выходим. Нет смысла что-то отправлять.

    if (commandPending)
    {
        SB_MAIN_STATUS_BAR->SimpleText = "Busy — waiting for ESP32 response...";
        return;
    }
    // Если ждём ответа на предыдущую команду — не отправляем новую.
    // Зачем? ESP32 обрабатывает команды последовательно.
    // Если засыпать её командами — она запутается.

    AnsiString cmd = command + "\n";
    // Добавляем \n в конец — ESP32 читает до символа новой строки.
    // Без \n ESP32 будет ждать ещё данных и не обработает команду.

    DWORD bytesWritten;

    if (WriteFile(hCom, cmd.c_str(), cmd.Length(), &bytesWritten, NULL))
    // cmd.c_str() — конвертируем AnsiString в обычную C-строку (const char*).
    // Windows API требует C-строку, не VCL AnsiString.
    // cmd.Length() — длина строки в байтах.
    {
        // Отправка прошла успешно — запоминаем что ждём ответ.
        commandPending   = true;
        lastCommand      = command;
        commandStartTime = GetTickCount();
        // GetTickCount() — время в мс от старта Windows.
        // Запомним когда отправили — для контроля таймаута в TimerReadComTimer.
    }
}

//---------------------------------------------------------------------------
// ParseData — разбирает ОДНУ строку пришедшую от ESP32.
//
// Вызывается из TimerReadComTimer для каждой строки отдельно.
// Каждое сообщение превращается в конкретное действие:
//   - обновление UI
//   - запуск программы
//   - открытие диалога выбора программы
//
// Параметр: data — одна строка от ESP32 (уже без \n)
//
// СТРУКТУРА ОБРАБОТКИ:
// Каждый блок if заканчивается return — как только нашли совпадение,
// обработали и вышли. Следующие блоки if не проверяются.
// Это и эффективно, и исключает случайные двойные срабатывания.
//---------------------------------------------------------------------------
void TForm1::ParseData(AnsiString data)
{
    data = data.Trim();
    // Trim() убирает пробелы, \r, \n по краям.
    // \r появляется потому что Windows добавляет \r перед \n (Windows-style перенос строки).

    if (data.IsEmpty()) return;
    // Пустую строку игнорируем — нечего обрабатывать.

    // ================================================================
    // СЛУЖЕБНЫЕ СООБЩЕНИЯ ESP32
    // Обычно не доходят до ParseData (перехватываются в CheckESP32),
    // но обрабатываем для надёжности.
    // ================================================================
    if (data == "ESP32_OK" || data == "ESP32_READY" || data == "CONNECTED_OK")
    {
        SB_MAIN_STATUS_BAR->SimpleText = "ESP32: " + data;
        return;
    }

    // ================================================================
    // КНОПКА D4 — КОРОТКОЕ НАЖАТИЕ
    // ESP32 отправляет "BTN_D4:1" когда кнопка D4 была нажата и отпущена
    // быстрее чем за 2 секунды (LONG_PRESS_MS).
    // ================================================================
    if (data == "BTN_D4:1")
    {
        SB_MAIN_STATUS_BAR->SimpleText = "D4 short press - launching: " +
                                          ExtractFileName(D4_ProgramPath);
        // ExtractFileName — VCL функция которая извлекает имя файла из полного пути.
        // ExtractFileName("C:\Windows\System32\calc.exe") вернёт "calc.exe".
        // Показываем короткое имя а не длинный путь — экономим место в строке статуса.

        ExecuteD4Program();  // Запускаем программу назначенную на D4
        return;
    }

    // ================================================================
    // КНОПКА D4 — ДЛИННОЕ НАЖАТИЕ (HOLD)
    // ESP32 отправляет "BTN_D4:HOLD" когда кнопка D4 удерживается
    // дольше 2000 мс (LONG_PRESS_MS) без отпускания.
    // Реакция: открываем диалог выбора новой программы для D4.
    // ================================================================
    if (data == "BTN_D4:HOLD")
    {
        SB_MAIN_STATUS_BAR->SimpleText = "D4 long press - opening dialog...";

        OpenDialog1->Title    = "Select program for D4 button";
        OpenDialog1->FileName = "";
        // Очищаем поле имени — диалог откроется без предвыбранного файла.

        if (OpenDialog1->Execute())
        // Execute() — показывает диалог выбора файла.
        // Возвращает true если пользователь ВЫБРАЛ файл и нажал "Открыть".
        // Возвращает false если пользователь нажал "Отмена".
        {
            D4_ProgramPath = OpenDialog1->FileName;
            // FileName — полный путь к выбранному файлу.
            // Например: "C:\Windows\System32\notepad.exe"

            UpdateD4PathDisplay();
            // Обновляем EDIT_D4 и LBL_D4_PINNED_TO с новым путём.

            SaveSettings();
            // Немедленно сохраняем в INI файл — не хотим потерять выбор.

            SB_MAIN_STATUS_BAR->SimpleText = "D4 now pinned to: " +
                                              ExtractFileName(D4_ProgramPath);
        }
        else
        {
            SB_MAIN_STATUS_BAR->SimpleText = "D4 - selection cancelled";
            // Пользователь нажал "Отмена" — ничего не меняем, сообщаем об этом.
        }
        return;
    }

    // ================================================================
    // КНОПКА D16 — КОРОТКОЕ НАЖАТИЕ
    // ================================================================
    if (data == "BTN_D16:1")
    {
        SB_MAIN_STATUS_BAR->SimpleText = "D16 short press - launching: " +
                                          ExtractFileName(D16_ProgramPath);
        ExecuteD16Program();
        return;
    }

    // ================================================================
    // КНОПКА D16 — ДЛИННОЕ НАЖАТИЕ (HOLD)
    // ================================================================
    if (data == "BTN_D16:HOLD")
    {
        SB_MAIN_STATUS_BAR->SimpleText = "D16 long press - opening dialog...";

        OpenDialog1->Title    = "Select program for D16 button";
        OpenDialog1->FileName = "";

        if (OpenDialog1->Execute())
        {
            D16_ProgramPath = OpenDialog1->FileName;
            UpdateD16PathDisplay();
            SaveSettings();
            SB_MAIN_STATUS_BAR->SimpleText = "D16 now pinned to: " +
                                              ExtractFileName(D16_ProgramPath);
        }
        else
        {
            SB_MAIN_STATUS_BAR->SimpleText = "D16 - selection cancelled";
        }
        return;
    }

    // ================================================================
    // КНОПКА D17 — КОРОТКОЕ НАЖАТИЕ
    // ================================================================
    if (data == "BTN_D17:1")
    {
        SB_MAIN_STATUS_BAR->SimpleText = "D17 short press - launching: " +
                                          ExtractFileName(D17_ProgramPath);
        ExecuteD17Program();
        return;
    }

    // ================================================================
    // КНОПКА D17 — ДЛИННОЕ НАЖАТИЕ (HOLD)
    // ================================================================
    if (data == "BTN_D17:HOLD")
    {
        SB_MAIN_STATUS_BAR->SimpleText = "D17 long press - opening dialog...";

        OpenDialog1->Title    = "Select program for D17 button";
        OpenDialog1->FileName = "";

        if (OpenDialog1->Execute())
        {
            D17_ProgramPath = OpenDialog1->FileName;
            UpdateD17PathDisplay();
            SaveSettings();
            SB_MAIN_STATUS_BAR->SimpleText = "D17 now pinned to: " +
                                              ExtractFileName(D17_ProgramPath);
        }
        else
        {
            SB_MAIN_STATUS_BAR->SimpleText = "D17 - selection cancelled";
        }
        return;
    }

    // ================================================================
    // ВСЁОСТАЛЬНОЕ — неизвестные сообщения
    // Показываем в строке статуса. Полезно при отладке — видим
    // все сообщения от ESP32 которые мы не ожидали.
    // ================================================================
    SB_MAIN_STATUS_BAR->SimpleText = "ESP32: " + data;
}

//---------------------------------------------------------------------------
// SetConnectedState — единая точка обновления UI при подключении/отключении.
//
// Вместо разбросанных по коду BTN_CONNECT->Enabled = false — всё собрано здесь.
// Вызывается из BTN_CONNECTClick (state=true) и BTN_DISCONNECTClick (state=false).
//
// Параметры:
//   state — true = только что подключились, false = только что отключились
//   port  — имя порта ("COM13") при подключении, "" при отключении
//---------------------------------------------------------------------------
void TForm1::SetConnectedState(bool state, AnsiString port)
{
    connected = state;
    // Обновляем флаг — теперь весь код знает о новом состоянии.

    if (state)
    {
        // ===== ПОДКЛЮЧИЛИСЬ К ESP32 =====

        LBL_CONNECTION_STATUS->Caption =
            "CONNECTION STATUS: CONNECTED to " + port;
        LBL_CONNECTION_STATUS->Font->Color = clGreen;
        // Font->Color — цвет шрифта метки. clGreen = зелёный.

        BTN_CONNECT->Enabled    = false;
        // Enabled = false — кнопка недоступна (серая).
        // Нельзя нажать CONNECT когда уже подключены.

        BTN_DISCONNECT->Enabled = true;
        // Теперь можно нажать DISCONNECT.

        CMB_COM_PORT->Enabled   = false;
        // Нельзя менять порт пока подключены — закрываем список.

        TimerReadCom->Enabled   = true;
        // ЗАПУСКАЕМ ТАЙМЕР — начинаем читать данные от ESP32 каждые 50 мс.

        Sleep(100);
        // Пауза 100 мс чтобы ESP32 успела выйти из режима обработки TEST
        // и начать нормально работать в loop().

        char cmd[] = "CONNECTED\n";
        DWORD bytesWritten;
        WriteFile(hCom, cmd, strlen(cmd), &bytesWritten, NULL);
        // Отправляем "CONNECTED\n" напрямую через WriteFile (не через SendCommand).
        // Почему не SendCommand? Потому что commandPending мог быть true
        // (мы только что получили ответ на TEST, но не сбросили флаг).
        // Прямой WriteFile обходит эту проверку.
        // ESP32 получит команду и обновит свой дисплей: "PC CONNECTED".
    }
    else
    {
        // ===== ОТКЛЮЧИЛИСЬ ОТ ESP32 =====

        LBL_CONNECTION_STATUS->Caption = "CONNECTION STATUS: DISCONNECTED";
        LBL_CONNECTION_STATUS->Font->Color = clRed;
        // clRed = красный — визуально показывает что нет подключения.

        BTN_CONNECT->Enabled    = true;   // Можно снова подключиться
        BTN_DISCONNECT->Enabled = false;  // Нечего отключать
        CMB_COM_PORT->Enabled   = true;   // Можно выбрать другой порт
        TimerReadCom->Enabled   = false;  // ОСТАНАВЛИВАЕМ ТАЙМЕР — порт закрыт

        commandPending = false;  // Сбрасываем ожидание — ответ уже не придёт
        lastCommand    = "";     // Очищаем последнюю команду
    }
}


